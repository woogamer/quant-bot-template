def generate_signal(market_data: dict, account_data: dict) -> list[dict]:
    """매매 시그널을 생성합니다.

    이 함수는 main.py가 주기적으로 호출합니다.
    market_data와 account_data를 분석하여 매수/매도 시그널을 반환하세요.

    Args:
        market_data: 관심 종목의 현재가 정보.
            예시:
            {
                "005930": {"현재가": 71000, "전일대비": -500, "등락률": -0.70},
                "000660": {"현재가": 123000, "전일대비": 1000, "등락률": 0.82},
            }

        account_data: 내 계좌 잔고 정보.
            예시:
            {
                "보유종목": [
                    {"종목코드": "005930", "종목명": "삼성전자", "수량": 10, "평균단가": 70000},
                ],
                "예수금": 1000000,
            }

    Returns:
        시그널 리스트. 매매할 게 없으면 빈 리스트 [] 를 반환하세요.
        각 시그널은 아래 형식을 따라야 합니다:

        [
            {
                "ticker": "005930",     # 종목코드 (문자열, 6자리)
                "action": "BUY",        # "BUY" 또는 "SELL"
                "qty": 1,               # 주문 수량 (정수)
                "price": 0,             # 지정가 (0이면 시장가)
            },
        ]
    """
    signals = []

    # ------------------------------------------------------------------
    # 여기에 본인의 매매 조건을 작성하세요!
    # 아래는 예시입니다. 삼성전자가 70000원 이하면 1주 매수.
    # ------------------------------------------------------------------

    samsung = market_data.get("005930")
    if samsung and samsung["현재가"] <= 70000:
        signals.append({
            "ticker": "005930",
            "action": "BUY",
            "qty": 1,
            "price": 0,  # 시장가
        })

    return signals


